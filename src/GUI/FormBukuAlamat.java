    private void hapusBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusBTActionPerformed
        try {
        if (idTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Silakan isi ID terlebih dahulu");
            return;
        }
            
        Object[] options = {"Iya", "Tidak"};
        int konfirmasi = JOptionPane.showOptionDialog(
                null,
                "Apakah Anda yakin ingin menghapus data?",
                "Konfirmasi Hapus Data",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[0]);
            if (konfirmasi == JOptionPane.YES_OPTION) {
                String sql = "DELETE FROM kontak WHERE id='"+idTF.getText()+"'";
                Connection conn = Koneksi.getConnection();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        Tampilkandata();
        kosong();
    }//GEN-LAST:event_hapusBTActionPerformed

    private void ubahBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahBTActionPerformed
        try {
            String pilihanJK = "";
            if (lakilakiRB.isSelected()) {
                pilihanJK = lakilakiRB.getText();
            } else if (perempuanRB.isSelected()) {
                pilihanJK = perempuanRB.getText();
            }
            
            if (namaTF.getText().isEmpty() || pilihanJK.isEmpty() || tanggalTF.getText().isEmpty()
                || agamaTF.getText().isEmpty() || teleponTF.getText().isEmpty()
                || emailTF.getText().isEmpty() || alamatTA.getText().isEmpty()
                || keteranganTA.getText().isEmpty() || idTF.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Semua kolom harus diisi");
                    return; 
                }
            
            String sql = "UPDATE kontak SET nama ="
                    + "'"+namaTF.getText()+"', jk = '"+pilihanJK+"',"
                    + "tanggal = '"+tanggalTF.getText()+"', agama = '"+agamaTF.getText()+"', telepon = '"+teleponTF.getText()+"',"
                    + "email = '"+emailTF.getText()+"', alamat = '"+alamatTA.getText()+"', keterangan = '"+keteranganTA.getText()+"'"
                    + "WHERE id = '"+idTF.getText()+"'";
            Connection conn = Koneksi.getConnection();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        Tampilkandata();
        kosong();
    }//GEN-LAST:event_ubahBTActionPerformed


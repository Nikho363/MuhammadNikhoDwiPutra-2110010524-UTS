    public void Tampilkandata() {
        //coding untuk meampilkan data dalam tabel
        daftarTBL_Kontak = new DefaultTableModel();
        daftarTBL_Kontak.addColumn("ID");
        daftarTBL_Kontak.addColumn("Nama");
        daftarTBL_Kontak.addColumn("Jenis Kelamin");
        daftarTBL_Kontak.addColumn("Tanggal Lahir");
        daftarTBL_Kontak.addColumn("Agama");
        daftarTBL_Kontak.addColumn("No. Telepon");
        daftarTBL_Kontak.addColumn("Email");
        daftarTBL_Kontak.addColumn("Alamat");
        daftarTBL_Kontak.addColumn("Keterangan");
        
        kontakTBL.setModel(daftarTBL_Kontak);
        Connection conn = Koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from kontak";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()){
                daftarTBL_Kontak.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("nama"),
                    res.getString("jk"),
                    res.getString("tanggal"),
                    res.getString("agama"),
                    res.getString("telepon"),
                    res.getString("email"),
                    res.getString("alamat"),
                    res.getString("keterangan") });
            }
        lebarkolom();
        
        //coding untuk menampilkan jumlah kontak yang ada pada tabel
        int i = daftarTBL_Kontak.getRowCount();
        jumlahKontakLB.setText(""+i);
        
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    

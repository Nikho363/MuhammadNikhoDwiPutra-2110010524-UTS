    private void kontakTBLMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kontakTBLMouseClicked
        int baris = kontakTBL.rowAtPoint(evt.getPoint());
        String id =kontakTBL.getValueAt(baris, 0).toString();
        idTF.setText(id);
        String nama =kontakTBL.getValueAt(baris, 1).toString();
        namaTF.setText(nama);
        
        String jenisKelamin = kontakTBL.getValueAt(baris, 2).toString();
        if (jenisKelamin.equals("Laki-Laki")) {
            lakilakiRB.setSelected(true);
            perempuanRB.setSelected(false);
        } else if (jenisKelamin.equals("Perempuan")) {
            lakilakiRB.setSelected(false);
            perempuanRB.setSelected(true);
        } else {
            lakilakiRB.setSelected(false);
            perempuanRB.setSelected(false);
        }
        
        String tanggal =kontakTBL.getValueAt(baris, 3).toString();
        tanggalTF.setText(tanggal);
        String agama =kontakTBL.getValueAt(baris, 4).toString();
        agamaTF.setText(agama);
        String telepon =kontakTBL.getValueAt(baris, 5).toString();
        teleponTF.setText(telepon);
        String email =kontakTBL.getValueAt(baris, 6).toString();
        emailTF.setText(email);
        String alamat =kontakTBL.getValueAt(baris, 7).toString();
        alamatTA.setText(alamat);
        String keterangan =kontakTBL.getValueAt(baris, 8).toString();
        keteranganTA.setText(keterangan);
    }//GEN-LAST:event_kontakTBLMouseClicked

